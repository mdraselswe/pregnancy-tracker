// --- Helper Functions ---
const toBengaliNumber = (num) => {
  if (num === undefined || num === null) return '০'
  const bengaliDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯']
  return String(num).replace(
    /[0-9]/g,
    (digit) => bengaliDigits[parseInt(digit)]
  )
}

